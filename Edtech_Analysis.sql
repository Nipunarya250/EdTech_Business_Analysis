-- SECTION 1: REVENUE ANALYSIS
-- ============================================================

-- Q1: Enrollment and Revenue Summary
-- Total enrollments, revenue, paid vs free trial split.

SELECT
    COUNT(e.enrollment_id) AS total_enrollments,
    SUM(p.amount) AS total_revenue,
    SUM(CASE WHEN e.enrollment_status = 'Paid' THEN 1 ELSE 0 END) AS total_paid,
    SUM(CASE WHEN e.enrollment_status = 'Free Trial' THEN 1 ELSE 0 END) AS total_free_trial
FROM enrollments e
LEFT JOIN payments p
    ON e.student_id = p.student_id
    AND e.course_id = p.course_id;


-- Q2: Revenue by Course Category

SELECT
    c.category,
    SUM(p.amount) AS total_category_revenue
FROM payments p
JOIN courses c ON p.course_id = c.course_id
GROUP BY c.category
ORDER BY total_category_revenue DESC;


-- Q3: Revenue by Plan Type

SELECT
    c.plan,
    SUM(p.amount) AS total_plan_revenue
FROM payments p
JOIN courses c ON p.course_id = c.course_id
GROUP BY c.plan
ORDER BY total_plan_revenue DESC;


-- Q4: Revenue by Payment Mode

SELECT
    p.payment_mode,
    SUM(p.amount) AS revenue_per_mode
FROM payments p
GROUP BY p.payment_mode
ORDER BY revenue_per_mode DESC;


-- Q5: Monthly Revenue Trend

SELECT
    YEAR(p.payment_date) AS revenue_year,
    MONTH(p.payment_date) AS revenue_month,
    SUM(p.amount) AS total_revenue
FROM payments p
GROUP BY revenue_year, revenue_month
ORDER BY revenue_year, revenue_month;


