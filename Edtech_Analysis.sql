-- Q14: Teacher Revenue Rank

WITH teacher_revenue AS (
    SELECT
        t.teacher_name,
        SUM(p.amount) AS revenue_per_teacher
    FROM payments p
    JOIN courses c ON p.course_id = c.course_id
    JOIN teachers t ON c.teacher_id = t.teacher_id
    GROUP BY t.teacher_id, t.teacher_name
)
SELECT
    teacher_name,
    revenue_per_teacher,
    RANK() OVER (ORDER BY revenue_per_teacher DESC) AS revenue_rank
FROM teacher_revenue;


-- Q15: Running Total of Monthly Revenue

WITH monthly_revenue AS (
    SELECT
        YEAR(p.payment_date) AS year_,
        MONTH(p.payment_date) AS month_,
        SUM(p.amount) AS month_total
    FROM payments p
    GROUP BY YEAR(p.payment_date), MONTH(p.payment_date)
)
SELECT
    year_,
    month_,
    month_total,
    SUM(month_total) OVER (ORDER BY year_, month_) AS running_total
FROM monthly_revenue
ORDER BY year_, month_;


-- Q16: Month-over-Month Revenue Comparison

WITH monthly_revenue AS (
    SELECT
        YEAR(p.payment_date) AS year_,
        MONTH(p.payment_date) AS month_,
        SUM(p.amount) AS revenue_of_month
    FROM payments p
    GROUP BY YEAR(p.payment_date), MONTH(p.payment_date)
)
SELECT
    year_,
    month_,
    revenue_of_month,
    LAG(revenue_of_month) OVER (ORDER BY year_, month_) AS revenue_prev_month
FROM monthly_revenue
ORDER BY year_, month_;
