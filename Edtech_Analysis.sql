-- Q10: Overall Lead Conversion Rate
--
-- Two versions included to demonstrate deduplication logic.
-- Without DISTINCT: 4,664 leads | 675 converted | 14%
-- With DISTINCT:    4,519 leads | 530 converted | 12%
-- The difference confirms duplicate lead records exist in the dataset.
-- Version B (DISTINCT) is the accurate figure.

-- Version A: Without DISTINCT
WITH base AS (
    SELECT
        l.lead_id,
        e.student_id
    FROM leads l
    LEFT JOIN enrollments e
        ON l.student_id = e.student_id
        AND e.enrollment_status = 'Paid'
),
summary AS (
    SELECT
        COUNT(lead_id) AS lead_count,
        COUNT(CASE WHEN student_id IS NOT NULL THEN 1 END) AS total_converted
    FROM base
)
SELECT
    lead_count,
    total_converted,
    CONCAT(ROUND((total_converted / lead_count) * 100.0, 0), '%') AS conversion_rate
FROM summary;


-- Version B: With DISTINCT (accurate)
WITH base AS (
    SELECT DISTINCT
        l.lead_id,
        e.student_id
    FROM leads l
    LEFT JOIN enrollments e
        ON l.student_id = e.student_id
        AND e.enrollment_status = 'Paid'
),
summary AS (
    SELECT
        COUNT(lead_id) AS lead_count,
        COUNT(CASE WHEN student_id IS NOT NULL THEN 1 END) AS total_converted
    FROM base
)
SELECT
    lead_count,
    total_converted,
    CONCAT(ROUND((total_converted / lead_count) * 100.0, 0), '%') AS conversion_rate
FROM summary;


-- Q11: Conversion Rate by Campaign Source
--
-- Note: Only 4,519 leads are in the dataset after cleanup.
-- Results reflect campaign-tracked leads only.

WITH base AS (
    SELECT DISTINCT
        c.campaign_source,
        l.lead_id,
        e.student_id
    FROM campaigns c
    JOIN leads l ON c.campaign_id = l.campaign_id
    LEFT JOIN enrollments e
        ON l.student_id = e.student_id
        AND e.enrollment_status = 'Paid'
),
summary AS (
    SELECT
        campaign_source,
        COUNT(lead_id) AS lead_count,
        SUM(CASE WHEN student_id IS NOT NULL THEN 1 ELSE 0 END) AS total_converted
    FROM base
    GROUP BY campaign_source
)
SELECT
    campaign_source,
    lead_count,
    total_converted,
    CONCAT(ROUND((total_converted / lead_count) * 100, 0), '%') AS conversion_rate
FROM summary
ORDER BY conversion_rate DESC;


-- Q12: Top 5 Campaigns by Conversion Count

WITH base AS (
    SELECT DISTINCT
        c.campaign_name,
        l.lead_id,
        e.student_id
    FROM campaigns c
    JOIN leads l ON c.campaign_id = l.campaign_id
    LEFT JOIN enrollments e
        ON l.student_id = e.student_id
        AND e.enrollment_status = 'Paid'
)
SELECT
    campaign_name,
    COUNT(lead_id) AS total_leads,
    SUM(CASE WHEN student_id IS NOT NULL THEN 1 ELSE 0 END) AS total_converted,
    CONCAT(
        ROUND(
            (SUM(CASE WHEN student_id IS NOT NULL THEN 1 ELSE 0 END) / COUNT(lead_id)) * 100.0,
        0),
        '%'
    ) AS conversion_rate
FROM base
GROUP BY campaign_name
ORDER BY total_converted DESC
LIMIT 5;


-- Q13: Average Days from Lead to Paid Enrollment
--
-- Result: 81 days average after correcting swapped columns in leads table.
-- Records where lead_date > enrollment_date excluded via WHERE filter.

SELECT
    ROUND(AVG(DATEDIFF(e.enrollment_date, l.lead_date)), 1) AS avg_days_to_convert
FROM leads l
JOIN enrollments e
    ON l.student_id = e.student_id
    AND e.enrollment_status = 'Paid'
WHERE l.lead_date <= e.enrollment_date;


